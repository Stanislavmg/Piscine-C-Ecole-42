int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = -1;
	while (++i, str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			continue ;
		else
			return (0);
	}
	return (1);
}
